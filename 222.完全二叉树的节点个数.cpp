/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    int countNodes(TreeNode* root) {
        if (!root)
            return 0;

        countNodes(root->left);
        countNodes(root->right);
        sum += 1;

        return sum;
    }

private:
    int sum = 0;
};
// @lc code=end

